def add(a,b):
    """
    :role : Additionner deux chiffres

    :param a: Le premier terme
    :param b: Le deuxième terme
    :return: La somme des deux termes
    """
    return a + b

def subtract(a,b):
    """
    :role : Soustraire un chiffre de l'autre

    :param a: Le soustrahend
    :param b: Le minuende
    :return: La différence entre le minuende et le soustrahend
    """
    return a - b 

def multiply(a,b):
    """
    :role : Multiplier deux chiffres ensemble

    :param a: Le premier facteur
    :param b: Le deuxième facteur
    :return: Le produit des deux facteurs
    """
    return a * b

def divide(a,b):
    """
    :role : Diviser un chiffre par l'autre

    :param a: Le dividende
    :param b: Le diviseur
    :return: Le quotient du dividende et du diviseur
    """
    return a // b
